include 'config.php';
include 'User.php';

$database = new Database();
$db = $database->getConnection();
$user = new User($db);

$message = "";

if ($_POST) {
    $user->username = trim($_POST['username']);
    $user->password = trim($_POST['password']);

    $result = $user->login();
    
    switch ($result) {
        case "success":
            header("Location: dashboard.php");
            exit();
        case "invalid_credentials":
            $message = '<div class="alert alert-danger">Invalid username/email or password.</div>';
            break;
        case "account_inactive":
            $message = '<div class="alert alert-warning">Your account is inactive. Please contact administrator.</div>';
            break;
        default:
            $message = '<div class="alert alert-danger">Login failed. Please try again.</div>';
    }
