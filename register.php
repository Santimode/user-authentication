<?php
// register.php
include 'config.php';
include 'User.php';

$database = new Database();
$db = $database->getConnection();
$user = new User($db);

$message = "";

if ($_POST) {
    $user->username = trim($_POST['username']);
    $user->email = trim($_POST['email']);
    $user->password = trim($_POST['password']);
    $confirm_password = trim($_POST['confirm_password']);

    // Validate passwords match
    if ($user->password !== $confirm_password) {
        $message = '<div class="alert alert-danger">Passwords do not match.</div>';
    } elseif (strlen($user->password) < 6) {
        $message = '<div class="alert alert-danger">Password must be at least 6 characters long.</div>';
    } else {
        $result = $user->register();
        
        switch ($result) {
            case "success":
                $message = '<div class="alert alert-success">Registration successful! You can now <a href="login.php" class="alert-link">login</a>.</div>';
                break;
            case "user_exists":
                $message = '<div class="alert alert-danger">Username or email already exists.</div>';
                break;
            default:
                $message = '<div class="alert alert-danger">Registration failed. Please try again.</div>';
        }
    }
}
